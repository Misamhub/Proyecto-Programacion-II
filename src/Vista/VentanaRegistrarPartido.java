    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // 1. Capturamos los nombres seleccionados en la interfaz gráfica
        String nombreLocal = cbEquipo1.getSelectedItem().toString();
        String nombreVisitante = cbEquipo2.getSelectedItem().toString();

        // Validación 1: Evitar que un equipo juegue contra sí mismo
        if (nombreLocal.equals(nombreVisitante)) {
            javax.swing.JOptionPane.showMessageDialog(this,
                    "Un equipo no puede jugar contra sí mismo en el mismo partido.",
                    "Error de Selección", javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Validación 2: Verificar que las cajas de texto no estén vacías
        if (txtGoles1.getText().trim().isEmpty() || txtGoles2.getText().trim().isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this,
                    "Por favor, ingresa los goles de ambos equipos.",
                    "Campos Vacíos", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        try {
            // 2. Convertimos los textos de los goles a números enteros
            int golesLocal = Integer.parseInt(txtGoles1.getText().trim());
            int golesVisitante = Integer.parseInt(txtGoles2.getText().trim());

            // Validación 3: Evitar números negativos de goles
            if (golesLocal < 0 || golesVisitante < 0) {
                javax.swing.JOptionPane.showMessageDialog(this,
                        "La cantidad de goles no puede ser un número negativo.",
                        "Error de Datos", javax.swing.JOptionPane.ERROR_MESSAGE);
                return;
            }

            // 3. BUSCAMOS LAS ENTIDADES EQUIPO EN LA LISTA DEL SERVICIO
            Entities.Equipo equipoLocal = null;
            Entities.Equipo equipoVisitante = null;

            for (Entities.Equipo eq : servicio.getEquipos()) {
                if (eq.getNombre().equals(nombreLocal)) {
                    equipoLocal = eq;
                }
                if (eq.getNombre().equals(nombreVisitante)) {
                    equipoVisitante = eq;
                }
            }

            // 4. Si encontramos las referencias, creamos el Partido y lo guardamos en el archivo real
            if (equipoLocal != null && equipoVisitante != null) {
                Entities.Partido nuevoPartido = new Entities.Partido(equipoLocal, equipoVisitante, golesLocal, golesVisitante);

                // Aquí el servicio procesa los puntos, goles y reescribe el archivo .properties
                servicio.registrarResultado(nuevoPartido);

                // Mensaje informativo real de éxito
                javax.swing.JOptionPane.showMessageDialog(this,
                        "¡Partido registrado con éxito!\nLos datos se guardaron en el archivo.",
                        "Registro Exitoso", javax.swing.JOptionPane.INFORMATION_MESSAGE);

                // 5. Regresamos a la ventana principal
                VentanaPrincipal principal = new VentanaPrincipal();
                principal.setLocationRelativeTo(null);
                principal.setVisible(true);
                this.dispose();
            } else {
                javax.swing.JOptionPane.showMessageDialog(this,
                        "Error interno: No se pudieron mapear las instancias de los equipos.",
                        "Error de Mapeo", javax.swing.JOptionPane.ERROR_MESSAGE);
            }

        } catch (NumberFormatException ex) {
            javax.swing.JOptionPane.showMessageDialog(this,
                    "Por favor, ingresa solo números enteros válidos en los campos de goles.",
                    "Error de Formato", javax.swing.JOptionPane.ERROR_MESSAGE);
        } catch (Exception ex) {
            // Captura los errores de validación de tu negocio
            javax.swing.JOptionPane.showMessageDialog(this,
                    ex.getMessage(),
                    "Validación de Campeonato", javax.swing.JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

