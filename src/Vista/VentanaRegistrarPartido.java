    private void colocarLogo() {
        try {
            // 1. Buscamos la ruta absoluta del recurso usando el paquete Vista
            java.net.URL stream = getClass().getResource("/Vista/logo.png");

            if (stream != null) {
                ImageIcon imagenOriginal = new ImageIcon(stream);

                // 2. Tomamos el tamaño exacto que le diste al cuadro naranja en el diseño
                int ancho = lblLogo.getWidth();
                int alto = lblLogo.getHeight();

                // Forzamos un tamaño ideal para el escudo en la parte superior
                if (ancho == 0 || alto == 0) {
                    ancho = 110;
                    alto = 110;
                }

                // 3. Escalamos la imagen para que rellene tu cuadro de forma perfecta
                Image imagenEscalada = imagenOriginal.getImage().getScaledInstance(ancho, alto, Image.SCALE_SMOOTH);
                ImageIcon logoFinal = new ImageIcon(imagenEscalada);

                // 4. Asignamos la imagen, la traemos al frente y repintamos el componente
                lblLogo.setIcon(logoFinal);
                lblLogo.repaint();

            } else {
                System.out.println("No se pudo encontrar el archivo logo.png dentro del paquete Vista.");
            }
        } catch (Exception e) {
            System.out.println("Error al cargar el logo en pantalla: " + e.getMessage());
        }
    }

    private void cargarEquiposEnCombos() {
        // 1. Limpiamos por completo los datos genéricos del diseñador visual
        cbEquipo1.removeAllItems();
        cbEquipo2.removeAllItems();

        try {
            // 2. Inicializamos el servicio si llega a estar en null para evitar errores
            if (servicio == null) {
                servicio = new ServicioCampeonato();
            }

            // 3. Recuperamos la lista de objetos Equipo cargados desde tu archivo .properties
            java.util.ArrayList<Entities.Equipo> listaEquipos = servicio.getEquipos();

            // 4. Recorremos las entidades y agregamos sus nombres reales a los JComboBox
            if (listaEquipos != null && !listaEquipos.isEmpty()) {
                for (Entities.Equipo eq : listaEquipos) {
                    cbEquipo1.addItem(eq.getNombre());
                    cbEquipo2.addItem(eq.getNombre());
                }
            } else {
                cbEquipo1.addItem("Sin equipos en archivo");
                cbEquipo2.addItem("Sin equipos en archivo");
            }

        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this,
                    "Error al leer los equipos del archivo real: " + e.getMessage(),
                    "Error de Carga", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }

