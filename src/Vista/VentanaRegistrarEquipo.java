    private void colocarLogo() {
        try {
            // 1. Buscamos la ruta del logo dentro del paquete Vista
            java.net.URL stream = getClass().getResource("/Vista/logo.png");

            if (stream != null) {
                ImageIcon imagenOriginal = new ImageIcon(stream);

                // 2. Tomamos el ancho y alto del lblLogo que pusiste en el diseño
                int ancho = lblLogo.getWidth();
                int alto = lblLogo.getHeight();

                // Valores de respaldo si el layout aún no ha calculado las dimensiones
                if (ancho == 0 || alto == 0) {
                    ancho = 130; // Ajusta este valor al ancho aproximado de tu cuadro naranja
                    alto = 82;   // Ajusta este valor al alto aproximado de tu cuadro naranja
                }

                // 3. Redimensionamos la imagen de forma suave (Smooth)
                Image imagenEscalada = imagenOriginal.getImage().getScaledInstance(ancho, alto, Image.SCALE_SMOOTH);
                ImageIcon logoFinal = new ImageIcon(imagenEscalada);

                // 4. Se lo asignamos al componente y refrescamos
                lblLogo.setIcon(logoFinal);
                lblLogo.repaint();
            } else {
                System.out.println("No se pudo encontrar el archivo logo.png en el paquete Vista.");
            }
        } catch (Exception e) {
            System.out.println("Error al cargar el logo en el panel: " + e.getMessage());
        }
    }
