    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String nombreEquipo = txtNombreEquipo.getText().trim();
        String datoEspecifico = txtDatoEspecifico.getText().trim();
        String tipoEquipo = cmbTipoEquipo.getSelectedItem().toString();

        // 1. Validar campos vacíos
        if (nombreEquipo.isEmpty() || datoEspecifico.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this,
                    "Por favor, completa todos los campos del formulario.",
                    "Campos Vacíos", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        try {
            // 2. Validar que no exista otro equipo igual
            for (Entities.Equipo eq : servicio.getEquipos()) { // Asegúrate de usar 'controlador' o 'servicio' según tu variable
                if (eq.getNombre().equalsIgnoreCase(nombreEquipo)) {
                    javax.swing.JOptionPane.showMessageDialog(this,
                            "Ya existe un equipo registrado con el nombre: " + nombreEquipo,
                            "Equipo Duplicado", javax.swing.JOptionPane.ERROR_MESSAGE);
                    return;
                }
            }

            // 3. ¡APLICACIÓN DE HERENCIA Y POLIMORFISMO!
            Entities.Equipo nuevoEquipo; // Declaramos la clase padre

            if (tipoEquipo.equals("Nacional")) {
                // Instanciamos la clase hija EquipoNacional
                nuevoEquipo = new Entities.EquipoNacional(nombreEquipo, datoEspecifico);
            } else {
                // Instanciamos la clase hija EquipoInternacional
                nuevoEquipo = new Entities.EquipoInternacional(nombreEquipo, datoEspecifico);
            }

            // 4. Se envía al servicio (él se encargará de guardarlo polimórficamente en el properties)
            servicio.registrarEquipo(nuevoEquipo);

            // 5. Notificar éxito
            javax.swing.JOptionPane.showMessageDialog(this,
                    "¡" + tipoEquipo + " '" + nombreEquipo + "' registrado con éxito!",
                    "Registro Exitoso", javax.swing.JOptionPane.INFORMATION_MESSAGE);

            // 6. Limpiar campos por si quiere registrar otro de inmediato
            txtNombreEquipo.setText("");
            txtDatoEspecifico.setText("");
            cmbTipoEquipo.setSelectedIndex(0);

        } catch (Exception ex) {
            javax.swing.JOptionPane.showMessageDialog(this,
                    "Error al guardar: " + ex.getMessage(),
                    "Error del Sistema", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

